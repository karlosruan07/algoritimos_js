
valor1 = 10
valor2 = 3

soma = valor1 + valor2
print(f'A soma será = {soma}')

subtração = valor1 - valor2
print(f'A subtração será = {subtração}')

multiplicação = valor1 * valor2
print(f'A multiplicação será = {multiplicação}')


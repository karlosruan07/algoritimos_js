print(f'A divisão será = {divisão}')

divisãoint = valor1 // valor2
print(f'A divisão inteira será = {divisãoint}')

resto = valor1 % valor2
print(f'O resto da divisão será = {resto}')

potencia = valor1 ** valor2
print(f'A potenciação será = {potencia}')